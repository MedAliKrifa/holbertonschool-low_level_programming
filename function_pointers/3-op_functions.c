#include "3-calc.h"
/**
 * op_add - op_add to add two int
 * @a: first in
 * @b: second int
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - op_sub to sub two int
 * @a: first in
 * @b: second int
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - op_mul to multip two int
 * @a: first in
 * @b: second int
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - op_div to div two int
 * @a: first in
 * @b: second int
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - op_mod to mod two int
 * @a: first in
 * @b: second int
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
